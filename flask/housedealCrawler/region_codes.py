    def get_all_codes(self):
        """전체 지역코드 리스트 반환"""
        all_codes = []
        for codes in self.all_region_codes.values():
            all_codes.extend(codes)
        return all_codes

    def get_codes_by_region(self, region_name):
        """특정 지역의 코드만 반환"""
        return self.all_region_codes.get(region_name, [])

    def get_major_cities_codes(self):
        """주요 도시 코드만 반환"""
        major_cities = ['seoul', 'busan', 'daegu', 'incheon', 'gyeonggi']
        codes = []
        for city in major_cities:
            codes.extend(self.all_region_codes.get(city, []))
        return codes

    def get_total_count(self):
        """전체 지역 수 반환"""
        return len(self.get_all_codes())

    def print_summary(self):
        """지역별 통계 출력"""
        print("=== 지역별 시군구 수 ===")
        for region, codes in self.all_region_codes.items():
            print(f"{region}: {len(codes)}개")
        print(f"전체: {self.get_total_count()}개")
